export function convertCategoryIdToAvatarIcon(id: number) {
  switch (id) {
    case 1:
      return "RulingNumber";
      break;
    case 2:
      return "OuterPersonNumber";
      break;
    case 3:
      return "SoulUrgeNumber";
      break;
    case 4:
      return "PersonalYearNumber";
      break;
    case 5:
      return "FengShui";
      break;
  }
}
